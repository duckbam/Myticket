package com.project.myticket.dto;

public class BookDTO {
	private String id;
	private String saleCode;
	private String bToday;
	private String bNum;
	private String bName;
	private String bWatch;
	private String bTicket;
	private String bCancel;
	private String bState;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getSaleCode() {
		return saleCode;
	}
	public void setSaleCode(String saleCode) {
		this.saleCode = saleCode;
	}
	public String getbToday() {
		return bToday;
	}
	public void setbToday(String bToday) {
		this.bToday = bToday;
	}
	public String getbNum() {
		return bNum;
	}
	public void setbNum(String bNum) {
		this.bNum = bNum;
	}
	public String getbName() {
		return bName;
	}
	public void setbName(String bName) {
		this.bName = bName;
	}
	public String getbWatch() {
		return bWatch;
	}
	public void setbWatch(String bWatch) {
		this.bWatch = bWatch;
	}
	public String getbTicket() {
		return bTicket;
	}
	public void setbTicket(String bTicket) {
		this.bTicket = bTicket;
	}
	public String getbCancel() {
		return bCancel;
	}
	public void setbCancel(String bCancel) {
		this.bCancel = bCancel;
	}
	public String getbState() {
		return bState;
	}
	public void setbState(String bState) {
		this.bState = bState;
	}
}
